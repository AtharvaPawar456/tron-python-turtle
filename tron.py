import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.pensize(8)
turtle.speed(5)
t.color("red")
t.lt(60)
t.fd(100)
t.rt(180)
t.rt(80)
t.fd(250)
t.lt(130)
t.fd(350)
t.goto(0,0)
t.lt(28)
t.bk(250)
t.fd(250)
t.rt(70)
t.fd(165)
t.lt(150)
t.fd(200)
t.lt(72)
t.fd(124)
t.goto(0,0)
t.rt(129)
t.fd(100)
t.goto(0,0)
t.lt(19)
t.rt(90)
t.penup()
t.fd(200)
t.pendown()
t.color("orange")
t.write("TRON",'false','center',font=('arabic',20))
t.lt(90)
t.bk(67)
t.penup()
t.fd(1000)
turtle.done()